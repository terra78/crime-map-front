/** 国籍名 → 色分け（日本: 青, 不明: グレー, その他（外国人）: オレンジ） */
export function getNationalityColor(nationality: string | undefined): { bg: string; text: string } {
  if (!nationality || nationality === '不明') return { bg: '#64748b33', text: '#64748b' }
  if (nationality === '日本') return { bg: '#4FC3F733', text: '#4FC3F7' }
  return { bg: '#FF704333', text: '#FF7043' }
}
